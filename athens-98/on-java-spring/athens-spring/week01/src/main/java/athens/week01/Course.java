package athens.week01;

public class Course {

    private String title;
    private int Days;
    private String tutor;

    public String getTitle() {
        return title;
    }

    public int getDays() {
        return Days;
    }

    public String getTutor() {
        return tutor;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setDays(int days) {
        Days = days;
    }

    public void setTutor(String tutor) {
        this.tutor = tutor;
    }

}
